/**
 * sprites.js - Zodiac pixel-art sprite data + renderer
 *
 * Exposes window.SPRITES  (lookup table) and
 *         window.renderSpriteGrid (drawing function).
 *
 * Grid encoding (12 cols x 16 rows per sprite, standard size):
 *   0 = transparent
 *   1 = primary colour (body fill)
 *   2 = secondary colour (eyes, snout, markings)
 *   3 = accent colour (highlight, inner tone)
 *   4 = dark shadow (darkened primary ~55%)
 *
 * Large grid encoding (24 cols x 32 rows, used when petSize === "large"):
 *   Same colour indices 0-4, 2x resolution with added shading detail.
 *
 * Body plan:
 *   Quadrupeds (cat, rat, ox, tiger, rabbit, horse, goat, dog, pig):
 *     Face LEFT by default. Head at cols 0-5, body spans to col 11.
 *     4 legs in rows 12-15 at cols 2-3, 4-5, 7-8, 9-10.
 *     Tail extends from right side.
 *     Growth is WIDER (not taller) across stages.
 *   Upright (classic, monkey, rooster):
 *     Face left. Grows wider/bigger across stages.
 *   Dragon: upright 2-legged, wings spread, spiky tail.
 *   Snake: horizontal S-curve, no legs.
 *
 * Cell size = Math.round(bodySize / COLS)
 * where bodySize = Math.round(BASE_SIZE * sizeMultiplier * STAGE_SCALES[stage])
 * BASE_SIZE = 24, sizeMultiplier: small=1.0, medium=1.5, large=2.0
 */


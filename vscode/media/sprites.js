  /**
   * Drop-in replacement for drawBody().
   * Renders the pixel-art grid for the given state at (x, bodyY).
   *
   * Reads petSize from document.body.dataset.petSize ("small" | "medium" | "large").
   *   small  -> 1.0x BASE_SIZE multiplier, uses 12x16 SPRITES
   *   medium -> 1.5x BASE_SIZE multiplier (default), uses 12x16 SPRITES
   *   large  -> 2.0x BASE_SIZE multiplier, uses 24x32 SPRITES_LG
   *
   * Uses the same STAGE_SCALES, STAGE_BODY_HEIGHT_MULTS, weightWidthMultiplier
   * and getPalette helpers already defined in sidebar.js (they are in the same
   * IIFE scope). This file is loaded BEFORE sidebar.js so it only exposes
   * the function on window; sidebar.js calls window.renderSpriteGrid().
   *
   * @param {CanvasRenderingContext2D} ctx
   * @param {object}  state
   * @param {number}  x          - left edge of bounding box
   * @param {number}  bodyY      - top of bounding box (after walk-bob)
   * @param {boolean} facingLeft
   * @param {number}  legFrame   - 0 or 1
   * @param {number}  breathPhase - current sleeping breath phase (radians)
   * @param {object}  STAGE_SCALES          - from sidebar.js scope
   * @param {object}  STAGE_BODY_HEIGHT_MULTS - from sidebar.js scope
   * @param {Function} weightWidthMultiplier  - from sidebar.js scope
   * @param {Function} getPalette             - from sidebar.js scope
   */
  function renderSpriteGrid(ctx, state, x, bodyY, facingLeft, legFrame, breathPhase,
                            STAGE_SCALES, STAGE_BODY_HEIGHT_MULTS, weightWidthMultiplier, getPalette) {

    var spriteType = state.spriteType || "classic";
    var stage      = state.stage      || "baby";
    var palette    = getPalette(state.color);
    var primary    = palette.primary;
    var secondary  = palette.secondary;
    var accent     = palette.secondary; // accent falls back to secondary

    // -- petSize multiplier ---------------------------------------------------
    var petSizeVal = (typeof document !== "undefined" && document.body && document.body.dataset)
                   ? (document.body.dataset.petSize || "medium")
                   : "medium";
    var sizeMultiplier = petSizeVal === "small" ? 1.0
                       : petSizeVal === "large"  ? 2.0
                       : 1.5; // medium (default)
    var useLarge = (petSizeVal === "large");

    // -- Derive darkened primary for colour index 4 --------------------------
    function darkenHex(hex, factor) {
      var h = hex.replace("#", "");
      if (h.length === 3) { h = h[0]+h[0]+h[1]+h[1]+h[2]+h[2]; }
      var r = Math.round(parseInt(h.substring(0,2),16) * factor);
      var g = Math.round(parseInt(h.substring(2,4),16) * factor);
      var b = Math.round(parseInt(h.substring(4,6),16) * factor);
      return "rgb(" + r + "," + g + "," + b + ")";
    }
    var dark = darkenHex(primary, 0.55);

    // -- Sick / sleeping colour overrides ------------------------------------
    if (state.sick)     { secondary = "#ff4444"; }
    if (state.sleeping) { secondary = "#888888"; }

    var stageScale  = STAGE_SCALES[stage] || 0.5;
    var bodySize    = Math.round(24 * sizeMultiplier * stageScale);
    var wt          = state.weight || 50;
    var wwm         = weightWidthMultiplier(wt);
    var bodyWidth   = Math.round(bodySize * wwm);
    var heightMult  = STAGE_BODY_HEIGHT_MULTS[stage] || 1.0;
    var bodyHeight  = Math.round(bodySize * heightMult);

    // Sleeping breath bob
    var bobY = bodyY;
    if (state.sleeping) {
      bobY = bodyY + Math.round(Math.sin(breathPhase) * 1);
    }

    // -- Egg: procedural rocking oval (no grid) ------------------------------
    if (stage === "egg") {
      ctx.save();
      var rockAngle = Math.sin(Date.now() / 600) * (5 * Math.PI / 180);
      var cx = x + bodyWidth / 2;
      var cy = bobY + bodyHeight / 2;
      ctx.translate(cx, cy);
      ctx.rotate(rockAngle);
      ctx.translate(-cx, -cy);
      ctx.fillStyle = primary;
      ctx.beginPath();
      ctx.ellipse(cx, cy, bodyWidth / 2, bodyHeight / 2, 0, 0, Math.PI * 2);
      ctx.fill();
      var dotSize   = Math.max(1, Math.round(bodySize * 0.10));
      var dotY      = bobY + Math.round(bodyHeight * 0.38);
      var dotLeftX  = x + Math.round(bodyWidth * 0.28);
      var dotRightX = x + Math.round(bodyWidth * 0.62);
      ctx.fillStyle = secondary;
      ctx.fillRect(dotLeftX,  dotY, dotSize, dotSize);
      ctx.fillRect(dotRightX, dotY, dotSize, dotSize);
      ctx.restore();
      return;
    }

    // -- Grid lookup ---------------------------------------------------------
    var grid;
    if (useLarge) {
      grid = (SPRITES_LG[spriteType] && SPRITES_LG[spriteType][stage])
           || (SPRITES_LG["classic"] && SPRITES_LG["classic"][stage]);
    } else {
      grid = (SPRITES[spriteType] && SPRITES[spriteType][stage])
           || (SPRITES["classic"] && SPRITES["classic"][stage]);
    }

    if (!grid) { return; }

    var COLS    = useLarge ? 24 : 12;
    var ROWS    = useLarge ? 32 : 16;
    var legRowStart = useLarge ? 30 : 14;

    var cellW = Math.max(1, Math.round(bodyWidth  / COLS));
    var cellH = Math.max(1, Math.round(bodyHeight / ROWS));

    // Map colour index to actual colour
    var colorMap = {
      1: primary,
      2: secondary,
      3: accent,
      4: dark
    };

    ctx.save();
    if (facingLeft) {
      ctx.translate(x + bodyWidth, 0);
      ctx.scale(-1, 1);
      ctx.translate(-x, 0);
    }

    for (var row = 0; row < ROWS; row++) {
      if (!grid[row]) { continue; }
      var isLegRow = (row >= legRowStart);

      for (var col = 0; col < COLS; col++) {
        var cell = grid[row][col];
        if (!cell) { continue; }

        var colOffsetY = 0;
        if (isLegRow) {
          var halfCols = COLS / 2;
          var isLeftLeg = col < halfCols;
          if (legFrame === 0) {
            colOffsetY = isLeftLeg ? 0 : 1;
          } else {
            colOffsetY = isLeftLeg ? 1 : 0;
          }
        }

        ctx.fillStyle = colorMap[cell] || primary;
        ctx.fillRect(
          x + col * cellW,
          bobY + row * cellH + colOffsetY,
          cellW,
          cellH
        );
      }
    }

    ctx.restore();
  }

